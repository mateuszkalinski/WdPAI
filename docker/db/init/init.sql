CREATE TABLE roles (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(30) NOT NULL UNIQUE,
    description TEXT
);

CREATE TABLE users (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    role_id INTEGER NOT NULL REFERENCES roles(id) ON DELETE RESTRICT,
    username VARCHAR(50) NOT NULL UNIQUE,
    email VARCHAR(255) NOT NULL UNIQUE,
    password TEXT NOT NULL,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    blocked_at TIMESTAMP WITH TIME ZONE,
    blocked_reason TEXT,
    last_login_at TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE UNIQUE INDEX users_email_lower_idx ON users (LOWER(email));

CREATE TABLE user_profiles (
    user_id INTEGER PRIMARY KEY REFERENCES users(id) ON DELETE CASCADE,
    firstname VARCHAR(100) NOT NULL,
    lastname VARCHAR(100) NOT NULL,
    bio TEXT,
    birth_date DATE,
    height_cm NUMERIC(5,2) CHECK (height_cm IS NULL OR height_cm > 0),
    body_weight_kg NUMERIC(5,2) CHECK (body_weight_kg IS NULL OR body_weight_kg > 0),
    experience_level VARCHAR(20) NOT NULL DEFAULT 'beginner'
        CHECK (experience_level IN ('beginner', 'intermediate', 'advanced')),
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE muscle_groups (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(80) NOT NULL UNIQUE,
    description TEXT
);

CREATE TABLE equipment (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(80) NOT NULL UNIQUE
);

CREATE TABLE exercises (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    equipment_id INTEGER REFERENCES equipment(id) ON DELETE SET NULL,
    name VARCHAR(120) NOT NULL UNIQUE,
    slug VARCHAR(140) NOT NULL UNIQUE,
    description TEXT NOT NULL,
    technique_notes TEXT,
    difficulty VARCHAR(20) NOT NULL DEFAULT 'beginner'
        CHECK (difficulty IN ('beginner', 'intermediate', 'advanced')),
    video_url TEXT,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE exercise_muscle_groups (
    exercise_id INTEGER NOT NULL REFERENCES exercises(id) ON DELETE CASCADE,
    muscle_group_id INTEGER NOT NULL REFERENCES muscle_groups(id) ON DELETE RESTRICT,
    involvement VARCHAR(20) NOT NULL DEFAULT 'primary'
        CHECK (involvement IN ('primary', 'secondary', 'stabilizer')),
    PRIMARY KEY (exercise_id, muscle_group_id)
);

CREATE TABLE workout_plans (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    owner_user_id INTEGER REFERENCES users(id) ON DELETE SET NULL,
    name VARCHAR(140) NOT NULL,
    description TEXT,
    goal VARCHAR(40) NOT NULL DEFAULT 'hypertrophy'
        CHECK (goal IN ('strength', 'hypertrophy', 'endurance', 'general')),
    level VARCHAR(20) NOT NULL DEFAULT 'beginner'
        CHECK (level IN ('beginner', 'intermediate', 'advanced')),
    is_template BOOLEAN NOT NULL DEFAULT FALSE,
    is_public BOOLEAN NOT NULL DEFAULT FALSE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE workout_plan_days (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workout_plan_id INTEGER NOT NULL REFERENCES workout_plans(id) ON DELETE CASCADE,
    name VARCHAR(120) NOT NULL,
    day_of_week SMALLINT CHECK (day_of_week BETWEEN 1 AND 7),
    day_order INTEGER NOT NULL CHECK (day_order > 0),
    notes TEXT,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (workout_plan_id, day_order)
);

CREATE TABLE workout_plan_exercises (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workout_plan_day_id INTEGER NOT NULL REFERENCES workout_plan_days(id) ON DELETE CASCADE,
    exercise_id INTEGER NOT NULL REFERENCES exercises(id) ON DELETE RESTRICT,
    exercise_order INTEGER NOT NULL CHECK (exercise_order > 0),
    target_sets INTEGER NOT NULL CHECK (target_sets > 0),
    target_reps_min INTEGER CHECK (target_reps_min IS NULL OR target_reps_min > 0),
    target_reps_max INTEGER CHECK (target_reps_max IS NULL OR target_reps_max > 0),
    target_rpe NUMERIC(3,1) CHECK (target_rpe IS NULL OR target_rpe BETWEEN 0 AND 10),
    rest_seconds INTEGER NOT NULL DEFAULT 90 CHECK (rest_seconds > 0),
    notes TEXT,
    UNIQUE (workout_plan_day_id, exercise_order),
    CHECK (
        target_reps_min IS NULL
        OR target_reps_max IS NULL
        OR target_reps_min <= target_reps_max
    )
);

CREATE TABLE user_workout_plans (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    workout_plan_id INTEGER NOT NULL REFERENCES workout_plans(id) ON DELETE CASCADE,
    custom_name VARCHAR(140),
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    assigned_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (user_id, workout_plan_id)
);

CREATE TABLE workout_sessions (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id INTEGER NOT NULL REFERENCES users(id) ON DELETE CASCADE,
    workout_plan_id INTEGER REFERENCES workout_plans(id) ON DELETE SET NULL,
    workout_plan_day_id INTEGER REFERENCES workout_plan_days(id) ON DELETE SET NULL,
    name VARCHAR(140) NOT NULL,
    status VARCHAR(20) NOT NULL DEFAULT 'planned'
        CHECK (status IN ('planned', 'in_progress', 'finished', 'cancelled')),
    started_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    finished_at TIMESTAMP WITH TIME ZONE,
    session_rpe NUMERIC(3,1) CHECK (session_rpe IS NULL OR session_rpe BETWEEN 0 AND 10),
    notes TEXT,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CHECK (finished_at IS NULL OR finished_at >= started_at)
);

CREATE TABLE performed_sets (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workout_session_id INTEGER NOT NULL REFERENCES workout_sessions(id) ON DELETE CASCADE,
    exercise_id INTEGER NOT NULL REFERENCES exercises(id) ON DELETE RESTRICT,
    set_order INTEGER NOT NULL CHECK (set_order > 0),
    set_type VARCHAR(20) NOT NULL DEFAULT 'working'
        CHECK (set_type IN ('warmup', 'working', 'drop', 'failure')),
    weight_kg NUMERIC(7,2) NOT NULL DEFAULT 0 CHECK (weight_kg >= 0),
    reps INTEGER NOT NULL CHECK (reps > 0),
    rpe NUMERIC(3,1) CHECK (rpe IS NULL OR rpe BETWEEN 0 AND 10),
    note TEXT,
    performed_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    UNIQUE (workout_session_id, exercise_id, set_order)
);

CREATE TABLE badges (
