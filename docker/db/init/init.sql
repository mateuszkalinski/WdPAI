CREATE OR REPLACE FUNCTION calculate_session_volume(p_session_id INTEGER)
RETURNS NUMERIC(12,2) AS $$
    SELECT COALESCE(SUM(weight_kg * reps), 0)::NUMERIC(12,2)
    FROM performed_sets
    WHERE workout_session_id = p_session_id;
$$ LANGUAGE SQL STABLE;

CREATE OR REPLACE FUNCTION award_exercise_weight_badges()
RETURNS TRIGGER AS $$
DECLARE
    session_user_id INTEGER;
BEGIN
    SELECT user_id INTO session_user_id
    FROM workout_sessions
    WHERE id = NEW.workout_session_id;

    INSERT INTO user_badges (user_id, badge_id, source_workout_session_id, current_value)
    SELECT
        session_user_id,
        b.id,
        NEW.workout_session_id,
        NEW.weight_kg
    FROM badges b
    WHERE b.is_active = TRUE
      AND b.criteria_type = 'exercise_weight'
      AND b.exercise_id = NEW.exercise_id
      AND NEW.weight_kg >= b.target_value
    ON CONFLICT (user_id, badge_id) DO UPDATE
    SET current_value = GREATEST(user_badges.current_value, EXCLUDED.current_value);

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

CREATE TRIGGER performed_sets_award_exercise_weight_badges
AFTER INSERT ON performed_sets
FOR EACH ROW EXECUTE FUNCTION award_exercise_weight_badges();

CREATE VIEW admin_user_overview AS
SELECT
    u.id,
    u.username,
    u.email,
    r.name AS role,
    u.is_active,
    u.blocked_at,
    u.created_at,
    p.firstname,
    p.lastname,
    COUNT(DISTINCT ws.id) FILTER (WHERE ws.status = 'finished') AS finished_workouts
FROM users u
JOIN roles r ON r.id = u.role_id
LEFT JOIN user_profiles p ON p.user_id = u.id
LEFT JOIN workout_sessions ws ON ws.user_id = u.id
GROUP BY u.id, r.name, p.firstname, p.lastname;

CREATE VIEW user_training_summary AS
SELECT
    u.id AS user_id,
    u.username,
    COUNT(DISTINCT ws.id) FILTER (WHERE ws.status = 'finished') AS finished_sessions,
    COALESCE(SUM(ps.weight_kg * ps.reps), 0)::NUMERIC(12,2) AS total_volume_kg,
    COUNT(ps.id) AS total_sets,
    ROUND(AVG(ps.rpe), 2) AS average_set_rpe,
    MAX(ws.started_at) FILTER (WHERE ws.status = 'finished') AS last_workout_at
FROM users u
LEFT JOIN workout_sessions ws ON ws.user_id = u.id
LEFT JOIN performed_sets ps ON ps.workout_session_id = ws.id
GROUP BY u.id, u.username;

CREATE VIEW weekly_muscle_group_summary AS
SELECT
    u.id AS user_id,
    u.username,
    DATE_TRUNC('week', ws.started_at)::DATE AS week_start,
    mg.id AS muscle_group_id,
    mg.name AS muscle_group,
    COUNT(ps.id) AS sets_count,
    COALESCE(SUM(ps.weight_kg * ps.reps), 0)::NUMERIC(12,2) AS volume_kg,
    ROUND(AVG(ps.rpe), 2) AS average_rpe
FROM users u
JOIN workout_sessions ws ON ws.user_id = u.id
JOIN performed_sets ps ON ps.workout_session_id = ws.id
JOIN exercises e ON e.id = ps.exercise_id
JOIN exercise_muscle_groups emg ON emg.exercise_id = e.id AND emg.involvement = 'primary'
JOIN muscle_groups mg ON mg.id = emg.muscle_group_id
WHERE ws.status = 'finished'
GROUP BY u.id, u.username, DATE_TRUNC('week', ws.started_at)::DATE, mg.id, mg.name;

CREATE VIEW exercise_usage_stats AS
WITH plan_usage AS (
    SELECT
        exercise_id,
        COUNT(*) AS planned_occurrences
    FROM workout_plan_exercises
    GROUP BY exercise_id
),
set_usage AS (
    SELECT
        exercise_id,
        COUNT(DISTINCT workout_session_id) AS sessions_used,
        COUNT(*) AS performed_sets,
        COALESCE(SUM(weight_kg * reps), 0)::NUMERIC(12,2) AS total_volume_kg,
        MAX(weight_kg) AS max_weight_kg
    FROM performed_sets
    GROUP BY exercise_id
)
SELECT
    e.id AS exercise_id,
    e.name AS exercise_name,
    eq.name AS equipment,
    COALESCE(pu.planned_occurrences, 0) AS planned_occurrences,
    COALESCE(su.sessions_used, 0) AS sessions_used,
    COALESCE(su.performed_sets, 0) AS performed_sets,
    COALESCE(su.total_volume_kg, 0)::NUMERIC(12,2) AS total_volume_kg,
    su.max_weight_kg
FROM exercises e
LEFT JOIN equipment eq ON eq.id = e.equipment_id
LEFT JOIN plan_usage pu ON pu.exercise_id = e.id
LEFT JOIN set_usage su ON su.exercise_id = e.id;

CREATE VIEW user_badge_overview AS
SELECT
    u.id AS user_id,
    u.username,
    b.id AS badge_id,
    b.name AS badge_name,
    b.criteria_type,
    b.target_value,
    ub.current_value,
    ub.awarded_at,
    ws.name AS source_session
FROM user_badges ub
JOIN users u ON u.id = ub.user_id
JOIN badges b ON b.id = ub.badge_id
LEFT JOIN workout_sessions ws ON ws.id = ub.source_workout_session_id;

